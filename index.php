                                <div class="newness_mobile"><img src="https://bumex.pl/images/home/newness_text.webp" width="475" height="700" class="zdj"></div>
								<p id="dif">W pełni nowatorskie urządzenie do chemicznego usuwania starych i zużytych powłok lakierniczych, opracowane i zaprojektowane przez zespół specjalistów firmy Bumex.</p>
								<p>Zastosowaliśmy innowacyjne rozwiązanie, polegające na opryskiwaniu przedmiotu poddanego procesowi odlakierowywania. Pozwoliło to uzyskać znaczne skrócenie czasu, w porównaniu do tradycyjnych metod, czyli zanurzaniu w czynniku aktywnym. Nasza nowa metoda pozwala przyspieszyć cykl odlakierowywania, a przez to znacznie zwiększa wydajność. Skrócenie cyklu to nie jedyna zaleta tego urządzenia. Kolejnym atutem jest zmniejszenie ilości czynnika aktywnego, koniecznej do działania urządzenia, w porównaniu z wannami wykorzystującymi metodę zanurzeniową.<br>
								Automat jest wykonywany w dwóch wersjach:<br>
								- z grzaniem bezpośrednim czynnika aktywnego,<br>
								- z grzaniem pośrednim, przy wykorzystaniu czynnika pośredniego (glikolu)<br>
								W obu przypadkach wykorzystywane są ramiona natryskowe, które dokonują oprysku od dołu i od góry umieszczonych na kracie elementów przeznaczonych do odlakierowywania.<br>
								Metoda napędu ramion natryskowych jest w pełni nowatorska i nie stosowana przez innych producentów. Ponadto wszystkie elementy, które mają kontakt z czynnikiem aktywnym, są wykonane z materiałów w pełni odpornych na jego działanie. Daje to gwarancję długiej i bezproblemowej eksploatacji urządzenia. Mocną stroną jest również duża energooszczędność. Zbiornik urządzenia jest w pełni izolowany. Ma to na celu zmniejszenia ilości energii, która jest potrzebna do utrzymania odpowiednio wysokiej temperatury zapewniającej optymalny proces odlakierowywania.<br>
								Wszystko to stawia nasz produkt w czołówce urządzeń przeznaczonych do procesów chemicznego usuwania powłok lakierniczych.
